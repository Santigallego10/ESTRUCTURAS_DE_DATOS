package co.edu.cue.algoritmosParecidosModuloSubasta;

public class Oferta {
    private int valor;
    private int numeroOfertante;

    public Oferta() {
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public int getNumeroOfertante() {
        return numeroOfertante;
    }

    public void setNumeroOfertante(int numeroOfertante) {
        this.numeroOfertante = numeroOfertante;
    }
}
